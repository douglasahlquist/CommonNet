package com.ahlquist.common.util;

public class NameValue {
	private String name;
	private String value;

	public NameValue(String _name, String _value) {
		this.name = _name;
		this.value = _value;
	}

	public String getName() {
		return name;
	}

	public String getValue() {
		return value;
	}
}
